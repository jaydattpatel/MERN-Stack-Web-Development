var i = 11;
console.log("----------while loop:");
while (i <= 13) {
  console.log(i, "while loop.");
  i++;
}

var i = 0;
console.log("----------do-while loop:");

do {
  console.log(i, "do-while loop.");
  i++;
} while (i < 5);
